public class Friend {
    private String name;
    private int age;
    private String hometown;
    private String workplace;

    public Friend(String name, int age, String hometown, String workplace) {
        this.name = name;
        this.age = age;
        this.hometown = hometown;
        this.workplace = workplace;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    public String getHometown() {
        return hometown;
    }

    public String getWorkplace() {
        return workplace;
    }
}

